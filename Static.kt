fun getClosestColor(hexColor: String): String {
    val targetRgb = hexToRgb(hexColor)
    var closestColor = ""
    var minDistance = Double.MAX_VALUE

    for ((hex, _) in colorMap) {
        val currentRgb = hexToRgb(hex)
        val distance = colorDistance(targetRgb, currentRgb)
        if (distance < minDistance) {
            minDistance = distance
            closestColor = hex
        }
    }
    return closestColor
}

fun hexToRgb(hex: String): Triple<Int, Int, Int> {
    val r = hex.substring(1, 3).toInt(16)
    val g = hex.substring(3, 5).toInt(16)
    val b = hex.substring(5, 7).toInt(16)
    return Triple(r, g, b)
}

fun colorDistance(rgb1: Triple<Int, Int, Int>, rgb2: Triple<Int, Int, Int>): Double {
    val rDiff = (rgb1.first - rgb2.first).toDouble()
    val gDiff = (rgb1.second - rgb2.second).toDouble()
    val bDiff = (rgb1.third - rgb2.third).toDouble()
    return Math.sqrt(rDiff * rDiff + gDiff * gDiff + bDiff * bDiff)
}
